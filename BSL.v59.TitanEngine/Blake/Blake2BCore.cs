        while (count > BlockSizeInBytes)
        {
            _counter0 += BlockSizeInBytes;
            if (_counter0 == 0) _counter1++;

            Compress(array, offset);
            offset += BlockSizeInBytes;
            count -= BlockSizeInBytes;
        }

        if (count <= 0) return;
        Array.Copy(array, offset, _buf, _bufferFilled, count);
        _bufferFilled += count;
    }

